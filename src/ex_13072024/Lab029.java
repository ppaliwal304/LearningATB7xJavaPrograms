package ex_13072024;

public class Lab029 {
    public static void main(String[] args) {


        // = Assignment Operator - Assign the value literal to the ref of data type

        // Arithmetic Operator +,-,/,*,
        // %
        int a = 10;
        int b = 23;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);

        // %
        System.out.println(a % b); // Remainders
    }
}
