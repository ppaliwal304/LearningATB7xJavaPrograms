package ex_13072024;

public class Lab024 {
    public static void main(String[] args) {
        //Operators
        System.out.println(2*2);//4
        System.out.println(2+2);//4
        System.out.println(2%2);// output 0
        System.out.println(15%2);// output 1 ,% modulus operator gives remainder

        // Arithmetic operators
        int a = 10;
        int b = 3;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
    }
}
