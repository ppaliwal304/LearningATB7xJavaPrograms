package ex_14072024;

public class Lab064 {
    public static void main(String[] args) {
        //Java Left Shift Operator Example multiply from 2 the powers

            System.out.println(10<<2);//10*2^2 =10*4=40 // 2*2
            System.out.println(10<<3);//10*2^3 =10*8=80 //2*2*2
            System.out.println(20<<2);//20*2^2 =20*4=80
            System.out.println(15<<4);//15*2^4 =15*16=240

        int x = -4;
        System.out.println(x<<1);//-4*2^1 =-8
    }
}
